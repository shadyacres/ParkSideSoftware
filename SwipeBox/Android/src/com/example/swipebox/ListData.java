package com.example.swipebox;

public class ListData 
{
	private String name;
	private String idNumber;
	
	public void setName(String refName)
	{
		name = refName;
	}
	
	public void setIdNumber(String refIdNumber)
	{
		idNumber = refIdNumber;
	}
	
	public String getName()
	{
		return name;
	}
	
	public String getIdNumber()
	{
		return idNumber;
	}

}